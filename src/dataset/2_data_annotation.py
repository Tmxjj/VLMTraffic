class VLMDataAnnotator:
    def __init__(self, data_path, output_path=None):
        self.data_path = data_path
        if output_path is None:
            self.output_path = os.path.join(os.path.dirname(data_path), "02_dataset_auto_annotated.jsonl")
        else:
            self.output_path = output_path
            
        self.nets = {}
        self.junction_edge_maps = {}

    def get_net_file_path(self, scenario):
        config = SCENARIO_CONFIGS.get(scenario)
        if not config:
            logger.error(f"Scenario {scenario} not found in SCENARIO_CONFIGS.")
            return None
            
        base_dir = f"data/raw/{scenario}"
        possible_paths = [
            os.path.join(PROJECT_ROOT, base_dir, "env", f"{config['NETFILE']}.net.xml"),
            os.path.join(PROJECT_ROOT, base_dir, f"{config['NETFILE']}.net.xml")
        ]
        for p in possible_paths:
            if os.path.exists(p):
                return p
        
        logger.error(f"Could not find net file for scenario {scenario}. Tried: {possible_paths}")
        return None

    def get_edge_directions(self, scenario, junction_id):
        cache_key = f"{scenario}_{junction_id}"
        if cache_key in self.junction_edge_maps:
            return self.junction_edge_maps[cache_key]

        net_path = self.get_net_file_path(scenario)
        if not net_path:
            return {}
            
        if scenario not in self.nets:
            self.nets[scenario] = sumolib.net.readNet(net_path, withPrograms=False, withLatestPrograms=False)
        net = self.nets[scenario]
        
        try:
            node = net.getNode(junction_id)
        except Exception as e:
            logger.error(f"Node {junction_id} not found in net file {net_path}.")
            return {}
            
        jx, jy = node.getCoord()
        
        direction_map = {}
        for edge in node.getIncoming():
            fx, fy = edge.getFromNode().getCoord()
            dx = fx - jx
            dy = fy - jy
            angle = math.degrees(math.atan2(dy, dx))
            
            # Map based on standard map layout directions
            if -45 < angle <= 45: dir_str = "East"
            elif 45 < angle <= 135: dir_str = "North"
            elif 135 < angle <= 180 or -180 <= angle <= -135: dir_str = "West"
            elif -135 < angle <= -45: dir_str = "South"
            else: dir_str = "Unknown"
            
            if dir_str != "Unknown":
                direction_map[dir_str] = edge.getID()
            
        self.junction_edge_maps[cache_key] = direction_map
        return direction_map

    def parse_vlm_counts(self, text):
        results = {}
        for direction in ["North", "South", "East", "West"]:
            # Looks for "Direction Approach: Lane 1... 12 ... Lane 2... 0 ... Lane 3... 4"
            pattern = rf"{direction} Approach:.*?Lane 1.*?(\d+).*?Lane 2.*?(\d+).*?Lane 3.*?(\d+)"
            match = re.search(pattern, text)
            if match:
                results[direction] = [int(g) for g in match.groups()]
            else:
                results[direction] = [0, 0, 0] # fallback if parsing fails
        return results

    def extract_gt_counts(self, gt_counts, edge_map, direction_lane_count=3):
        results = {}
        for dir_str in ["North", "South", "East", "West"]:
            edge_id = edge_map.get(dir_str)
            if not edge_id:
                results[dir_str] = [0] * direction_lane_count
                continue
            
            # In SUMO, lane 0 is outer (curb), lane N-1 is inner (median)
            # Lane 1 (Innermost) corresponds to SUMO index N-1
            # Lane 2 corresponds to N-2
            # Lane 3 (Outermost) corresponds to N-3 (which is 0 if N=3)
            counts = []
            N = direction_lane_count
            for l_idx in range(1, N + 1):
                sumo_idx = N - l_idx
                key = f"{edge_id}_{sumo_idx}"
                counts.append(gt_counts.get(key, 0))
                
            results[dir_str] = counts
        return results

    def populate_template(self, template, gt_mapping):
        lines = template.strip().split('\n')
        new_lines = []
        for line in lines:
            for dir_str in ["North", "South", "East", "West"]:
                if line.lstrip().startswith(f"{dir_str} Approach:"):
                    counts = gt_mapping.get(dir_str, [])
                    # Replace sequentially for however many <int> exist in this line
                    for c in counts:
                        if "<int>" in line:
                            line = line.replace("<int>", str(c), 1)
                        else:
                            break
                    break
            new_lines.append(line)
        return "\n".join(new_lines)
        
    def correct_vlm_text(self, vlm_text, scenario, gt_mapping):
        template = PromptBuilder.get_cot_lane_template(scenario)
        correct_lane_text = self.populate_template(template, gt_mapping)
        
        # Regex to replace Lane Analysis block safely respecting surrounding blocks
        pattern = re.compile(r"(- Lane Analysis \(Mandatory\):\s*\n)(.*?)(?=\s*- Phase Mapping:)", re.DOTALL)
        
        def replace_func(match):
            return f"{match.group(1)}{correct_lane_text}\n\n"
            
        new_text = pattern.sub(replace_func, vlm_text)
        return new_text

    def compute_error(self, vlm_counts, gt_counts):
        total_error = 0
        for dir_str in ["North", "South", "East", "West"]:
            vc = vlm_counts.get(dir_str, [])
            gc = gt_counts.get(dir_str, [])
            for i in range(max(len(vc), len(gc))):
                v_val = vc[i] if i < len(vc) else 0
                g_val = gc[i] if i < len(gc) else 0
                total_error += abs(v_val - g_val)
        return total_error

    def process(self):
        logger.info(f"Start processing {self.data_path}")
        if not os.path.exists(self.data_path):
            logger.error(f"Input file not found: {self.data_path}")
            return
            
        processed_count = 0
        
        with open(self.data_path, 'r', encoding='utf-8') as fin:
            content = fin.read()
            
        # The objects in dataset.jsonl are separated by "-----"
        chunks = [c.strip() for c in re.split(r'\n-----\n+', content) if c.strip()]
        
        with open(self.output_path, 'w', encoding='utf-8') as fout:
            for chunk in tqdm(chunks, desc=f"Annotating {os.path.basename(self.data_path)}"):
                try:
                    data = json.loads(chunk)
                except json.JSONDecodeError as e:
                    logger.warning(f"Skipping a chunk due to JSON parse error: {e}")
                    continue
                
                scenario = data.get("scenario")
                junction_id = data.get("junction_id")
                vlm_raw = data.get("vlm_response_raw")
                gt_sensor_counts = data.get("gt_vehicle_counts", {})
                
                if not all([scenario, junction_id, vlm_raw]) or "Scene Understanding:" not in vlm_raw:
                    # write as is if incomplete 
                    fout.write(json.dumps(data, indent=4) + "\n-----\n\n\n\n\n")
                    continue
                    
                # 1. Parse VLM output
                vlm_counts = self.parse_vlm_counts(vlm_raw)
                
                # 2. Get edge to direction mapping
                edge_map = self.get_edge_directions(scenario, junction_id)
                
                # 3. Extract GT counts formatted to directions
                # Assume 3 lanes per approach based on template
                gt_counts = self.extract_gt_counts(gt_sensor_counts, edge_map, direction_lane_count=3)
                
                # 4. Calculate error
                error_score = self.compute_error(vlm_counts, gt_counts)
                
                # 5. Generate corrected response text
                corrected_vlm_raw = self.correct_vlm_text(vlm_raw, scenario, gt_counts)
                
                # Update data
                data["step_2_vlm_response_raw"] = corrected_vlm_raw
                data["error_score"] = error_score
                
                fout.write(json.dumps(data, indent=4) + "\n-----\n\n\n\n\n")
                processed_count += 1
                
        logger.info(f"Processed {processed_count} records. Output saved to {self.output_path}")

